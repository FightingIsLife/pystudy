import numpy as np

A = np.ones((3, 1))  # 3x1
B = np.ones((1, 4))  # 1x4

# 观察广播结果
print("广播前形状：", A.shape, B.shape)
print("广播结果形状：", (A + B).shape)  # 应得(3,4)

A = np.array([
    [[1, 2, 3]],  # 层0
    [[4, 5, 6]],  # 层1
    [[7, 8, 9]],  # 层2
    [[10, 11, 12]]  # 层3
])

B = np.array([
    [
        [0.1, 0.2, 0.3],  # 行0
        [0.4, 0.5, 0.6],  # 行1
        [0.7, 0.8, 0.9],  # 行2
        [1.0, 1.1, 1.2],  # 行3
        [1.3, 1.4, 1.5]  # 行4
    ],
])

print(A * B)
